# madlibs game
# a word game where you create a story
# by filling in blanks with random words

adjective1 = input("Enter an adjective (description): ")
noun1 = input("Enter a noun (person, place, or thing): ")
adjective2 = input("Enter another adjective (description): ")
verb1 = input("Enter a verb (action, ending in -ing): ")
adjective3 = input("Enter another adjective (description): ")

print(f"Today, I went to a {adjective1} zoo.")
print(f"In an exhibit, I saw the {noun1}.")
print(f"The {noun1} was {adjective2} and {verb1}.")
print(f"I was {adjective3}.")

